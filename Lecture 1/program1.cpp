// Sum of 2 numbers
#include <iostream>
using namespace std;

int main(){
    int a,b;
    cout << "Enter a: ";
    cin >> a;

    cout << "Enter b: ";
    cin >> b;

    cout << "Sum of 2 no. is: " << a + b << endl;
    cout << "Difference of 2 no. is: " << a - b << endl;
    cout << "Multiply of 2 no. is: " << a * b << endl;
    cout << "Division of 2 no. is: " << a / b << endl;
    cout << "Modulo of 2 no. is: " << a % b << endl;
    return 0;
}