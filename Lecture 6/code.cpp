#include <iostream>
using namespace std;

int main(){
    int a = 10, b = 2;

    // Bitwise Operator

    // And Operator
    cout << (a & b) << endl;
    // Or Operator
    cout << (a | b) << endl;
    // XOR Operator
    cout << (a ^ b) << endl;
    // Left Shift Operator
    cout << (a << b) << endl;
    // Right Shift Operator
    cout << (a >> b) << endl;
    return 0;
}